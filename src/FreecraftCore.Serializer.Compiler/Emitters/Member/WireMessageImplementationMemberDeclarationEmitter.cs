		private SyntaxToken[] ComputeModifiers(bool requiresOverride)
		{
			if (requiresOverride)
				return new[]
				{
					Token
					(
						TriviaList
						(
							Whitespace("		")
						),
						SyntaxKind.PublicKeyword,
						TriviaList
						(
							Space
						)
					),
					Token
					(
						TriviaList(),
						SyntaxKind.OverrideKeyword,
						TriviaList
						(
							Space
						)
					)
				};
			else
			{
				//Case where it's a sealed type, we CANNOT have virtuals
				if (TypeSymbol.IsSealed)
				{
					return new[]
					{
						Token
						(
							TriviaList
							(
								Whitespace("		")
							),
							SyntaxKind.PublicKeyword,
							TriviaList
							(
								Space
							)
						)
					};
				}
				else
				{
					return new[]
					{
						Token
						(
							TriviaList
							(
								Whitespace("		")
							),
							SyntaxKind.PublicKeyword,
							TriviaList
							(
								Space
							)
						),
						Token
						(
							TriviaList(),
							SyntaxKind.VirtualKeyword,
							TriviaList
							(
								Space
							)
						)
					};
				}
			}
		}

		private ITypeSymbol GetRootWireMessageType()
		{
			var lastType = TypeSymbol.BaseType;
			var t = TypeSymbol.BaseType;

			while (t != null && t.BaseType != null && t.IsWireMessageType())
			{
				if (!t.IsWireMessageType())
					return lastType;

				lastType = t;
				t = t.BaseType;
			}

			return lastType;
		}
